package trackyt.android.client.models;

public class AuthResponse {
	private boolean login;
	private String token;
	
	public AuthResponse(String token, boolean login) {
		this.token = token;
		this.login = login;
	}
	
	public String getToken() {
		return token;
	}
	
	public boolean getLogin() {
		return login;
	}
}
